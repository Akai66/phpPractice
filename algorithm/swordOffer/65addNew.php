//思路:每一位相加后,先忽略进位,单独记录进位数,最后将相加的结果和进位数相加.
//转换为二进制,每一位相加忽略进位等同于做异或运算,单独计算进位等同于做与运算,之后再进行左移1位操作

function addNew($num1,$num2){
    while($num2 != 0){
        $sum = $num1 ^ $num2;
        $carry = ($num1 & $num2) << 1;
        $num1 = $sum;
        $num2 = $carry;
    }
    return $num1;
}

var_dump(addNew(14,18));